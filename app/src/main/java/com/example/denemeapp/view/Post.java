package com.example.denemeapp.view;

public class Post {

    public  String description;
    public  String downloadurl;
    public  String email;
    public  String salary;
    public  String title;

    public Post(String description, String downloadurl, String email, String salary, String title) {
        this.description = description;
        this.downloadurl = downloadurl;
        this.email = email;
        this.salary = salary;
        this.title = title;
    }

}
